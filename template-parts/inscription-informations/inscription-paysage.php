    <h2>
        RELATION À UN PAYSAGE

        <!-- bulle d'information -->
        <img class="tooltip-icon" src="<?php echo get_stylesheet_directory_uri(); ?>/dist/assets/images/info-bulle.svg">
    </h2>
    <span class="tooltip-text">Le paysage peut définir un rêve bien plus que l’action ou ses personnages. Le souvenir du lieu, comme dans la vie éveillée, peut être la seule sensation prégnante, d’une ambiance qui nous reste d’un rêve.
Mais est-ce que les paysages de notre vie de veille influent sur nos rêves? Et les paysages de notre enfance, de notre vie actuelle, de nos désirs? Est-ce qu’une personne qui, dans l’enfance, vivait au pied d’une montagne, percevant une nature plus grande mais abordable, rêve autre qu’une personne qui dormait, enfant, en bord de mer, devant une nature plate, infinie et impalpable? On dit qu’on se métamorphose en notre environnement : à la campagne, nos mains sont calleuses comme la terre, en ville nos corps élancés à la manière de longs immeubles, et pour nos rêves?
<br>
1.En corse, les mazzeri sont des chasseurs oniriques et prophétiques liés à des paysages typiques, qu’ils connaissent bien. En rêve, après avoir chassé l’animal dans une nature opaque, souvent miroir de leur environnement diurne, le museau de la victime se transforme en visage d’humain, celui d’un homme ou d’une femme qu’il connait, qui va bientôt mourir. Au mazzeru d’agir…
<br>
2.Attestée dans les trois grandes religions monothéistes et jusqu’à la Grèce Antique, l’incubation consiste à s’endormir dans un lieu donné -  fontaine, grotte, temple, afin d’y recevoir un rêve prophétique, une vision, un message, une aide. De la même manière, partir en vacances dans une maison de campagne, lorsqu’on vit habituellement en ville, permettrait-il des rêves d’une autre nature?
</span>
