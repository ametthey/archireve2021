        La question de l’âge permet de situer l’archireveur·euse sur deux plans à la fois : son âge biologique approximatif (comment les rêves peuvent évoluer en vieillissant) et sa situation générationnelle (quels événements, quelles idées, ont pu le marquer durablement).
        <br>
1.On sait que “Le fœtus rêve beaucoup plus que le nourrisson qui, lui-même, rêve bien plus que l’enfant, et ainsi de suite.” (Tobie Nathan, «La nouvelle interprétation des rêves», p.152). Aussi, la fonction du rêve est différente selon l’âge. Selon David Foulkes, l’enfant de moins de 7 ans est très rarement impliqué émotionnellement dans ses rêves et se positionne comme un simple spectateur. On constate aussi une prédominance d’animaux, de fins magiques, d’activités plus extravagantes et plus fournies en agression que ceux des adultes.
<br>
2.Comment une crise s’immisce dans les rêves d’une population? Quels sont les thèmes, les sensations et les émotions récurrents dans leurs rêves? Peut-on prédire un événement redoutable à l’échelle d’un groupe? Charlotte Beradt, en 1933, collecte les rêves de 300 berlinois et cherche, au-delà de l’individualité du rêve, des schémas et des parentés. Elle constate qu’en cette période, les rêves sont très fortement marqués par les persécutions et la terreur insidieuse qui s’installe - comme des rêves prémonitoires préparant l’inconscient à la guerre à venir. Hervé Mazurel et Elisabeth Serin ont récemment collecté les rêves de français·es en période de confinement : ils rapportent une augmentation de 15% de rêves négatifs (peur, intrusion, enfermement) et 7 % positifs (liberté, promenades en famille, fêtes).
<br>
<br>
La question de l’âge permettrait de constater des différences dans les rêves, entre les âges des individus, mais aussi de comprendre des schémas globaux de perceptions inconscientes ou de ressentis collectifs lors d'événements impactants, nationaux ou mondiaux.
