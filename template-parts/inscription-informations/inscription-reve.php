    <span class="tooltip-text">
        Les rêves nous parlent, nous mettent en garde, ou nous font jouer des scènes déstabilisantes. En répondant à ces deux questions, des multitudes d’hypothèses sont possibles.  Est ce que la fréquence de souvenir de rêve augmente leur qualité narrative? Est ce qu’un type de rêve se dégage chez ceux·celles qui y prêtent moins attention? Et est-ce que la lecture d’autres rêves vient enrichir l’imaginaire onirique?
        <br>
        1.On observe dans notre rapport aux rêves une différence interculturelle. Dans la religion des Aborigènes d’Australie, l’univers est né du temps du rêve. Barbara Glowczewski le désigne comme un espace-temps parallèle à notre temps profane. Alors, chaque élément naturel est la trace d’êtres ancestraux qui ont modelé la terre. Ces empreintes ont donné naissance à des rêves particuliers (rêve du Serpent Arc-en-ciel, rêve des Hommes Kangourou, rêve des Peuples Nuages ou rêve d'Ignames, rêve Frères Vent ou rêve Femmes Bâton à fouir.). Ils ont été transmis de génération en génération par l’intermédiaire de mythes, de chants, de peintures corporelles… Ainsi chaque Aborigène est gardien d’un rêve et se doit de l’entretenir et de le transmettre.
        <br>
        Il y a une grande différence entre les cultures: dans nos sociétés contemporaines occidentales, le rêve est traité de manière neuronale, scientifique, où, l’imagination, le soin, occupent une place minime. Par ailleurs, on a vu, durant la crise du Covid-19, un regain de travaux artistiques, de récits, d’intérêts aux rêves, montrant l’importance vitale de cette vie nocturne pour notre inconscient.
        <br>
        2.A travers cette question, nous notons aussi les différences inter-personnelles.  En effet, chaque archirêveur·euse possède un rapport intime particulier à ses rêves. Certains·es les considères comme de simples restitutions de leur journée sous forme d’images loufoques, d’autres en parlent au sein de leur groupe ou les écrivent chaque matin et y trouve des significations particulières, quelques-uns·unes les transforment en oeuvres d’art, plusieurs·es travaillent leur lucidité afin de soigner des maux ou l’exploitent avec leur thérapeute.
        <br>
        Pratiquement, écrire chaque jour ses rêves, en parler et y travailler permettrait un dialogue intime avec le rêve et des rappels plus précis et récurrents. Ainsi, notre rapport onirique influence la nature même du rêve que l’on va produire, sa remémoration ou la possibilité d’un rêve lucide - mais dans quelle mesure?
        <br>
“Un rêve qui s’évanouit est comme un fruit qu’on n’a pas cueilli.” Tobie Nathan.
