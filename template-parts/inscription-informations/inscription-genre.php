    <span class="tooltip-text">
        La question du genre permet de percevoir l'archireveur·euse dans sa fluidité, sans le·la stigmatiser.
        <br>
1.Le genre a toujours été une des données (avec l’âge) sur laquelle les premières études oniriques (Hall et Van de Castle, 1951) permettaient de pointer les différences pouvant exister dans le contenu de rêves entre les individus et à l’époque donc, de signifier des disparités entre hommes et femmes.
L’ambition est ici de remettre à jour ces études tout en élargissant la question aux nombreux genres non pris en compte à l’époque, ainsi  que la notion de fluidité. Il serait par exemple intéressant de noter les différentes perceptions du sexe et du genre qu’une personne a d’elle-même entre sa vie diurne et onirique. Percevoir son sexe/genre, ou le sexe/genre d’une personne qu’on désire dans son rêve peut être interessant lors de questionnement d’identité.
<br>
2.Des articles, comme ceux de Maurice Stauffachner dans la Revue Médicale Suisse,  soulignent que si nous avons la mémoire d'une identité de genre qui nous évite de nous réinventer chaque jour à notre réveil, la nuit « les processus cognitifs se relâchent et se ressaisissent tour à tour » : les murs du récit de l’identité s’abaissent pour un temps et accueillent en leur sein les désirs d’être différent. C’est «l’expression d’une saine curiosité transgressant les barrières culturelles » mises en place dans les sociétés, peut-être l’expression la plus évidente d’une empathie pour ce qui nous est étranger.

