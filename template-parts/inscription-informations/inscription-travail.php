    </h2>
    <span class="tooltip-text">
    Le temps, la nature et la façon dont nous nous sentons au travail influencent la qualité du sommeil et peuvent être facteurs de nombreuses projections.
    <br>
1.Une étude allemande datant de 2009, propose qu’1 rêve sur 5 concerne notre travail. Selon Tristan Moir, psychanalyste et spécialiste de l’interprétation des rêves, beaucoup de rêveurs se voient dans des situations de mise en échec, permettant à l’inconscient de revivre le stress accumulé, afin d’envisager les difficultés et de les apprivoiser. Il rapporte aussi des rêves de réussite et de toute puissance - comme chez les enfants, chez qui les rêves glorifiants permettent de se convaincre qu’ils pourront maîtriser un jour le monde. S’imaginer patron à la place du patron serait alors un moyen compensatoire inconscient.
<br>
2.Dans les années 1950, des scientifiques anglo-saxons ont fondé une banque de rêves. Elle révèle notamment des différences selon les professions. Mais de manière générale,  la part des rêves de travail a explosé en une cinquantaine d’années. Le burn-out, expression de l’évolution du monde du travail dans la société contemporaine, conduit à la dépression, à l’épuisement et donc se répercute largement sur notre vie nocturne. Des travailleurs d'Amazon ont ainsi rapporté une porosité entre leur activité salariale et leur sommeil. Travaillant avec une oreillette leur permettant de recevoir des indications pour les prochains colis, certains salariés continuent à entendre dans leurs rêves cette même voix, invariablement présente.
    Cette question pourrait permettre d’entrevoir le rapport entre le type de hiérarchie, de fonctionnement, de lien au travail avec les sensations liées aux rapports humains au sein du rêve. Aussi, comment rêve-t-on en cette période de télétravail, d’étude à distance, d’augmentation du chômage, de l’explosion du travail en indépendant?
    </span>
