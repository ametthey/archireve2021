    </h2>

    <span class="tooltip-text">
        <b>animisme</b> : Conception générale qui attribue aux êtres de l'univers, aux choses, une âme analogue à l'âme humaine.<br>
        <b>théisme</b> : Doctrine qui affirme l'existence d'un Dieu personnel, cause du monde.<br>
        <b>déisme</b> : Doctrine religieuse qui rejette toute révélation et ne croit qu'à l'existence d'un Dieu comme cause du monde et à la religion naturelle.<br>
        <b>agnosticisme</b> : Doctrine qui considère que l'absolu est inaccessible à l'esprit humain et qui préconise le refus de toute solution aux problèmes métaphysiques.<br>
        <b>athéisme</b> : Doctrine qui nie l'existence de Dieu.<br>
        <b>panthéiste</b> : Philosophie d'après laquelle tout ce qui existe est identifié à Dieu<br>

        <br>

        Le relation de l’homme et des dieux est dans le sang du rêve, “le rêve est animisme par nature.” (Tobie Nathan, «La nouvelle interprétation des rêves», p. 219).

        <br>
        1.C’est à travers ce monde connecté au divin, à l’immatériel, à l’au-delà, que l’on voyait et que l’on voit toujours le sens du rêve : une aide, un message, une prophétie. Tous les peuples ont un rapport intime au rêve et son message, son utilisation religieuse en est le plus pratique : incubation, medecine-dream, onirocritie, rêve lucide, cultes des morts, apparition, etc.
Les différentes temporalités proposées permettent d’appréhender, au-delà des symboles perçus dans le rêve, le rapport possible à la pratique sacrée entre l’archireveur·euse et son monde onirique.
Pour Tobie Nathan, en occident, les rites mortuaires païens, primordiaux, ont été écrasés par l’Eglise puis soufflés par la Science et les morts renaissent alors dans nos rêves, dans l’attente d’un deuil rituel, du rétablissement d’un ordre. “Les morts apaisés donnent aux vivants; les morts suspendus, mal morts, mal enterrés ou sans funérailles, les morts maltraités, quelquefois de manière contraire à leur tradition, ceux-là viennent exiger leur dû aux vivants“ dans le monde du rêve.
        <br>
        2.Georges Devereux raconte que pour les Indiens Mohave de Californie, chaque maladie a été préétablie lors de la création du monde. Lorsque le medecine-man entreprend de soigner un malade, c’est qu’il a assisté en rêve à sa création. “Ainsi, lorsque les armes à feu firent leur apparition et infligèrent des blessures par balles, un chaman rêva bientôt qu’il avait été témoin de la phase de la création du monde qui se rapporte à l’établissement primordial, prototype et constituant un précédent de toute blessure par une arme à feu et de sa guérison (Georges Devereux, «Les rêves pathogènes dans les sociétés non occidentales», p. 195).
    </span>

